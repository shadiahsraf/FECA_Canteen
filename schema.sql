-- search_path includes `extensions` because Supabase installs pgcrypto
-- (digest) there rather than in `public`.
create or replace function verify_admin(p_password text) returns boolean
language sql security definer set search_path = public, extensions as $$
  select coalesce(
    (select admin_hash = encode(digest(p_password, 'sha256'), 'hex') from app_secrets where id = 1),
    false);
$$;
revoke all on function verify_admin(text) from public;

-- ----------------------------------------------------------------------------
--  process_sale — atomic checkout.
--  Locks each product row, rejects insufficient stock, uses DATABASE prices
--  (never client-supplied prices), writes the sale + items, decrements stock,
--  and audit-logs — all in one transaction.
--  p_items: [{"id": "<uuid>", "quantity": 2}, ...]
-- ----------------------------------------------------------------------------
create or replace function process_sale(p_items jsonb, p_seller text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_sale     sales;
  v_item     jsonb;
  v_product  products;
  v_qty      integer;
  v_total    numeric(12,2) := 0;
  v_log      jsonb := '[]'::jsonb;
begin
  if p_items is null or jsonb_typeof(p_items) <> 'array' or jsonb_array_length(p_items) = 0 then
    raise exception 'EMPTY_CART';
  end if;

  -- Pass 1: lock + validate every line, total from DB prices.
  for v_item in select * from jsonb_array_elements(p_items) loop
    v_qty := (v_item->>'quantity')::integer;
    if v_qty is null or v_qty <= 0 or v_qty > 1000 then
      raise exception 'BAD_QUANTITY';
    end if;
    select * into v_product from products
      where id = (v_item->>'id')::uuid and is_deleted = false
      for update;
    if not found then
      raise exception 'PRODUCT_NOT_FOUND';
    end if;
    if v_product.quantity < v_qty then
      raise exception 'INSUFFICIENT_STOCK:%:%', v_product.name, v_product.quantity;
    end if;
    v_total := v_total + (v_product.price * v_qty);
  end loop;

  insert into sales (total_price, seller_name)
    values (v_total, coalesce(p_seller, 'seller'))
    returning * into v_sale;

  -- Pass 2: write items + decrement stock (rows already locked).
  for v_item in select * from jsonb_array_elements(p_items) loop
    v_qty := (v_item->>'quantity')::integer;
    select * into v_product from products where id = (v_item->>'id')::uuid;
    insert into sale_items (sale_id, product_id, quantity, price_at_sale)
      values (v_sale.id, v_product.id, v_qty, v_product.price);
    update products set quantity = quantity - v_qty where id = v_product.id;
    v_log := v_log || jsonb_build_object('product', v_product.name, 'qty', v_qty, 'price', v_product.price);
  end loop;

  insert into logs (user_name, action, details)
    values (coalesce(p_seller, 'seller'), 'sale',
            jsonb_build_object('sale_id', v_sale.id, 'order_no', v_sale.order_no,
                               'total', v_total, 'items', v_log));

  return to_jsonb(v_sale);
end $$;

revoke all on function process_sale(jsonb, text) from public;
grant execute on function process_sale(jsonb, text) to anon;

-- ----------------------------------------------------------------------------
--  refund_sale — admin-verified refund. Restores stock, marks the sale
--  refunded (never deletes it), audit-logs. Refusing double refunds.
-- ----------------------------------------------------------------------------
create or replace function refund_sale(p_sale_id uuid, p_password text, p_user text, p_reason text default null)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_sale sales;
  v_item record;
begin
  if not verify_admin(p_password) then
    raise exception 'INVALID_ADMIN_PASSWORD';
  end if;

  select * into v_sale from sales where id = p_sale_id for update;
  if not found then
    raise exception 'SALE_NOT_FOUND';
  end if;
  if v_sale.status = 'refunded' then
    raise exception 'ALREADY_REFUNDED';
  end if;

  -- Restore stock for every line that still points at a product.
  for v_item in select product_id, quantity from sale_items where sale_id = p_sale_id and product_id is not null loop
    update products set quantity = quantity + v_item.quantity where id = v_item.product_id;
  end loop;

  update sales
     set status = 'refunded', refunded_at = now(),
         refunded_by = coalesce(p_user, 'admin'), refund_reason = p_reason
   where id = p_sale_id
   returning * into v_sale;

  insert into logs (user_name, action, details)
    values (coalesce(p_user, 'admin'), 'refund',
            jsonb_build_object('sale_id', v_sale.id, 'order_no', v_sale.order_no,
                               'total', v_sale.total_price, 'reason', p_reason));

  return to_jsonb(v_sale);
end $$;

revoke all on function refund_sale(uuid, text, text, text) from public;
grant execute on function refund_sale(uuid, text, text, text) to anon;

-- ----------------------------------------------------------------------------
--  update_product_price — admin-verified price change, logs old → new.
-- ----------------------------------------------------------------------------
create or replace function update_product_price(p_id uuid, p_price numeric, p_password text, p_user text default 'admin')
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_old numeric(10,2);
  v_row products;
begin
  if not verify_admin(p_password) then
    raise exception 'INVALID_ADMIN_PASSWORD';
  end if;
  if p_price is null or p_price < 0 or p_price > 100000 then
    raise exception 'BAD_PRICE';
  end if;

  select price into v_old from products where id = p_id for update;
  if not found then
    raise exception 'PRODUCT_NOT_FOUND';
  end if;

  update products set price = p_price where id = p_id returning * into v_row;

  insert into logs (user_name, action, details)
    values (coalesce(p_user, 'admin'), 'price_change',
            jsonb_build_object('product_id', p_id, 'name', v_row.name,
                               'old_price', v_old, 'new_price', p_price));

  return to_jsonb(v_row);
end $$;

revoke all on function update_product_price(uuid, numeric, text, text) from public;
grant execute on function update_product_price(uuid, numeric, text, text) to anon;

-- ----------------------------------------------------------------------------
--  close_day — daily closing. Totals are computed IN the database for the
--  Cairo calendar day, the report row is immutable (unique per day, no UPDATE
--  policy), and the closing is audit-logged.
-- ----------------------------------------------------------------------------
create or replace function close_day(p_actual_cash numeric, p_password text, p_user text default 'admin', p_notes text default null)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_date   date := (now() at time zone 'Africa/Cairo')::date;
  v_total  numeric(12,2);
  v_count  integer;
  v_report daily_reports;
begin
  if not verify_admin(p_password) then
    raise exception 'INVALID_ADMIN_PASSWORD';
  end if;
  if p_actual_cash is null or p_actual_cash < 0 then
    raise exception 'BAD_CASH_AMOUNT';
  end if;
  if exists (select 1 from daily_reports where report_date = v_date) then
    raise exception 'DAY_ALREADY_CLOSED';
  end if;

  select coalesce(sum(total_price), 0), count(*)
    into v_total, v_count
    from sales
   where status = 'completed'
     and (created_at at time zone 'Africa/Cairo')::date = v_date;

  insert into daily_reports (report_date, total_sales, orders_count, expected_cash,
                             actual_cash, difference, closed_by, notes)
    values (v_date, v_total, v_count, v_total, p_actual_cash,
            p_actual_cash - v_total, coalesce(p_user, 'admin'), p_notes)
    returning * into v_report;

  insert into logs (user_name, action, details)
    values (coalesce(p_user, 'admin'), 'daily_close',
            jsonb_build_object('date', v_date, 'total_sales', v_total, 'orders', v_count,
                               'actual_cash', p_actual_cash, 'difference', p_actual_cash - v_total));

  return to_jsonb(v_report);
end $$;

revoke all on function close_day(numeric, text, text, text) from public;
grant execute on function close_day(numeric, text, text, text) to anon;

