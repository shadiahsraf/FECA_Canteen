-- =============================================================================
--  Canteen POS — Supabase / PostgreSQL schema  (v2 — production hardened)
--  Run this in the Supabase dashboard → SQL Editor → New query → Run.
--
--  Fresh install: run this whole file.
--  Existing database (v1): run migration.sql instead.
--
--  v2 adds: audit logs, daily closing reports, soft delete, refunds,
--  human-friendly order numbers, non-negative stock enforcement, and atomic
--  SECURITY DEFINER functions so every financial action is validated AND
--  logged inside the database — not just in the browser.
-- =============================================================================

create extension if not exists pgcrypto;   -- for gen_random_uuid() + digest()

-- ----------------------------------------------------------------------------
--  Tables
-- ----------------------------------------------------------------------------
create table if not exists products (
  id          uuid primary key default gen_random_uuid(),
  name        text not null check (length(trim(name)) between 1 and 120),
  price       numeric(10,2) not null default 0 check (price >= 0),
  category    text,
  image_url   text,
  quantity    integer not null default 0 check (quantity >= 0),  -- stock can never go negative
  code        text,
  is_deleted  boolean not null default false,                    -- soft delete only
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

create table if not exists sales (
  id            uuid primary key default gen_random_uuid(),
  order_no      bigint generated by default as identity unique,  -- human-friendly invoice number
  total_price   numeric(10,2) not null default 0 check (total_price >= 0),
  seller_name   text,
  status        text not null default 'completed' check (status in ('completed','refunded')),
  refunded_at   timestamptz,
  refunded_by   text,
  refund_reason text,
  created_at    timestamptz not null default now()
);

create table if not exists sale_items (
  id             uuid primary key default gen_random_uuid(),
  sale_id        uuid not null references sales(id) on delete cascade,
  product_id     uuid references products(id) on delete set null,
  quantity       integer not null default 1 check (quantity > 0),
  price_at_sale  numeric(10,2) not null default 0 check (price_at_sale >= 0)
);

-- Append-only audit trail. RLS below allows INSERT + SELECT only — rows can
-- never be updated or deleted through the API.
create table if not exists logs (
  id          bigint generated by default as identity primary key,
  user_name   text not null default 'unknown',
  action      text not null,          -- product_add / product_update / product_delete /
                                      -- price_change / sale / refund / daily_close / login
  details     jsonb not null default '{}'::jsonb,
  created_at  timestamptz not null default now()
);

-- One immutable closing report per calendar day (Africa/Cairo).
create table if not exists daily_reports (
  id            bigint generated by default as identity primary key,
  report_date   date not null unique,
  total_sales   numeric(12,2) not null default 0,
  orders_count  integer not null default 0,
  expected_cash numeric(12,2) not null default 0,
  actual_cash   numeric(12,2) not null default 0,
  difference    numeric(12,2) not null default 0,   -- actual − expected
  closed_by     text,
  notes         text,
  created_at    timestamptz not null default now()
);

create index if not exists idx_sale_items_sale     on sale_items(sale_id);
create index if not exists idx_sale_items_product  on sale_items(product_id);
create index if not exists idx_sales_created       on sales(created_at desc);
create index if not exists idx_products_category   on products(category);
create index if not exists idx_logs_created        on logs(created_at desc);

-- Keep products.updated_at fresh automatically.
create or replace function touch_updated_at() returns trigger
language plpgsql as $$
begin
  new.updated_at := now();
  return new;
end $$;

drop trigger if exists trg_products_touch on products;
create trigger trg_products_touch before update on products
  for each row execute function touch_updated_at();

-- ----------------------------------------------------------------------------
--  Row Level Security
--
--  No Supabase Auth — every request arrives as `anon`. The policies below are
--  deliberately granular:
--    • NOTHING can be deleted through the API (no DELETE policy on any table).
--    • logs + daily_reports are append-only (INSERT + SELECT, never UPDATE).
--    • sale_items can never be rewritten after a sale (INSERT + SELECT only).
--  Financial mutations (checkout, refunds, price changes, daily close) go
--  through the SECURITY DEFINER functions further down, which validate and
--  audit-log server-side.
-- ----------------------------------------------------------------------------
alter table products      enable row level security;
alter table sales         enable row level security;
alter table sale_items    enable row level security;
alter table logs          enable row level security;
alter table daily_reports enable row level security;

drop policy if exists p_products_all    on products;
drop policy if exists p_sales_all       on sales;
drop policy if exists p_sale_items_all  on sale_items;
drop policy if exists p_products_select on products;
drop policy if exists p_products_insert on products;
drop policy if exists p_products_update on products;
drop policy if exists p_sales_select    on sales;
drop policy if exists p_sales_insert    on sales;
drop policy if exists p_sale_items_select on sale_items;
drop policy if exists p_sale_items_insert on sale_items;
drop policy if exists p_logs_select     on logs;
drop policy if exists p_logs_insert     on logs;
drop policy if exists p_reports_select  on daily_reports;
drop policy if exists p_reports_insert  on daily_reports;

create policy p_products_select on products for select to anon using (true);
create policy p_products_insert on products for insert to anon with check (true);
create policy p_products_update on products for update to anon using (true) with check (true);

create policy p_sales_select on sales for select to anon using (true);
create policy p_sales_insert on sales for insert to anon with check (true);
-- no UPDATE policy on sales: refunds go through refund_sale() below.

create policy p_sale_items_select on sale_items for select to anon using (true);
create policy p_sale_items_insert on sale_items for insert to anon with check (true);

create policy p_logs_select on logs for select to anon using (true);
create policy p_logs_insert on logs for insert to anon with check (true);

create policy p_reports_select on daily_reports for select to anon using (true);
create policy p_reports_insert on daily_reports for insert to anon with check (true);

